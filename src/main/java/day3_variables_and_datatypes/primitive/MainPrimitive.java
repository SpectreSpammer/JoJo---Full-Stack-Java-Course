package day3_variables_and_datatypes.primitive;

public class MainPrimitive {
    public static void main(String[] args) {


        //8 bit
        byte age = 28;
        System.out.println("Age: " + age);

        //16 bit
        short departmentCode = 1234;
        System.out.println("Department Code : " + departmentCode);

        //16 bit
        char performanceGrade = 'A';
        System.out.println("Performance Grade : " + performanceGrade);

        //32 bit
        int employeeId = 1234567890;
        System.out.println("Employee Id : " + employeeId);

        //32 bit
        float hourlyRate = 300.751f;
        System.out.println("Hourly Rate : " + hourlyRate);

        //64 bit
        double salary = 50000.751231231;
        System.out.println("Salary : " + salary);

        //boolean
        boolean isFTE = false;
        System.out.println("is Full Time Employee : " + isFTE);



    }
}
